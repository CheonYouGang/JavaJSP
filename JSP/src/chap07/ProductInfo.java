package chap07;

public class ProductInfo {

	private String name;
	private int value;
	
	public ProductInfo() {
		
	}
	
	/******getter******/
	public String getName() {
		return name;
	}
	public int getValue() {
		return value;
	}
	/******getter******/
	
	/******setter******/
	public void setName(String name) {
		this.name = name;
	}
	public void setValue(int value) {
		this.value = value;
	}
	/******setter******/
}
